char ler_senha(){
	char perfil = 0; //perfil 0 (senha invalida), perfil 1 , perfil 2, perfil 3 (ADM)
	char senha[10]={'\0'}, i=0, j=0, letra = '\0';
	lcdSolicitaSenha();
	while(letra!='#'&&(j<10)){
		for(i=1;i<=4;i++){
			letra=scan(i);
			if (letra == 'd')
			return letra;			//se scan retornar valor atribuido ao comando de desligamento, interrompe a funcao aqui

			if('\0'!=letra && '*' != letra && '#' != letra){
				letra_lcd('*');
				senha[j] = letra;
				j++;
			}
			else if (letra == '*'){
				j = 0;
				while (senha[j+1]!='\0'){
					senha[j] = '\0';
					j++;
				}
				j=0;
				lcdSolicitaSenha();
			}
		}
	}
	if (!compara_string(senha, SENHA1)){
		perfil = 1;
		return perfil;
	}
	if (!compara_string(senha, SENHA2)){
		perfil = 2;
		return perfil;
	}
	if (!compara_string(senha, SENHAADM)){
		perfil = 3;
		return perfil;
	}
	else {
		perfil = 0;
		return perfil;
	}
	return perfil;
}


//loop principal
void homicros(char *perfil, flag *flag){
	char letra, verificacao, i = 1;
	if (*perfil == 0 || *perfil == 'd'){
		return;
	}
	while(1){
		letra = scan(i);
		verificacao = verifica_logoff();
		if (verificacao == '#'){
			limpa_lcd();
			escreve_lcd("Logoff Realizado");
			HAL_Delay(2000);
			*perfil = 0;
			return;
		}
		if (verificacao == '*'){
			desligaSistema(flag);
			*perfil = 'd';
			return;
		}



		i++;
		if (i > 4) 	i = 1;
	}

}

void login (flag *flag){
	char perfil = 0;
	while(1){
		perfil = ler_senha();

		if (perfil == 1){
			limpa_lcd();
			escreve_lcd("Perfil 1 Logado!");
			HAL_Delay(2000);

		}
		if (perfil == 2){
			limpa_lcd();
			escreve_lcd("Perfil 2 Logado!");
			HAL_Delay(2000);

		}
		if (perfil == 3){
			limpa_lcd();
			escreve_lcd("Perfil ADM Logado!");
			HAL_Delay(2000);

		}
		if (perfil == 0){
			limpa_lcd();
			escreve_lcd("Senha Invalida!");
			HAL_Delay(2000);				// atraso em que o usuario espera pra poder digitar novamente a senha
			lcdSolicitaSenha();
		}

		homicros(&perfil, flag); // loop principal

		if (perfil == 'd'){				//comando de desligar o sistema
			desligaSistema(flag);
			return;
		}

	}
}


int main(void){
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();

  char verificacao;
  flag flag;
  flag.sistema = 0;

  inicia();


  while (1) {
  	verificacao = verifica_logoff();
  	if (verificacao == '*'){
  		desligaSistema(&flag);
  	}
  	if (verificacao == '#' && flag.sistema == 0){
  		ligaSistema(&flag);
  		login(&flag);
  	}
  }
}

